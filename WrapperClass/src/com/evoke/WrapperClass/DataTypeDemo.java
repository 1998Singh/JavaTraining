package com.evoke.WrapperClass;

public class DataTypeDemo {
	public static void main(String[] args) {

		Boolean b = false;   // true or false
		byte a = 123;         // rang -128 to 127 
		short s = 1000;      //  range -32768 to 32767
		int i = 100000;      // (-2^31 to 2^31-1)
		float f = 234.5f;    // used for decimal values     range is unlimited
		char c = 'A';        // used to store character value    range 

		System.out.println(b);
		System.out.println(a);
		System.out.println(s);
		System.out.println(i);
		System.out.println(f);
		System.out.println(c);

	}

}
